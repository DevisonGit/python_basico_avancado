"""
+, -, *, /, //, **, %, ()
"""
print('Adicao +', 10 + 10)
print('Subtracao -', 10 - 5)
print('Multiplicacao *', 10 * 10)
print('Divisao / ', 10 / 3)
print('Divisao //', 10 // 3)
print('Exponenciacao **', 3 ** 3)
print('Resto de Divisao %', 10 % 2)
print('Sem precedencia', 5 + 2 * 10)
print('Com precedencia', (5 + 2) * 10)
