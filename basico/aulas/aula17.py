"""
Formatando String
Fatiamento de String [inicio:fim:passo]
Funcoes built-in
"""

#posisitivos [012345678]
nome        ='Python_S2'
#negativos   [987654321]

print('Pegando indice 2:', nome[2])
print('Pegando indice -2:', nome[-2])
print('Pegando partes:', nome[0::2])

print('Deixando tudo minusculo: ', nome.lower())
print('Deixando tudo maisculo:', nome.upper())

